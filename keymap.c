void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_MAIN);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_FN1);
        update_tri_layer(_FN1, _FN2, _FN3);
      } else {
        layer_off(_FN1);
        update_tri_layer(_FN1, _FN2, _FN3);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_FN2);
        update_tri_layer(_FN1, _FN2, _FN3);
      } else {
        layer_off(_FN2);
        update_tri_layer(_FN1, _FN2, _FN3);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_FN3);
      } else {
        layer_off(_FN3);
      }
      return false;
      break;
  }
  return true;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
    case _FN1:
        rgblight_setrgb (0x00,  0x00, 0xFF);
        break;
    case _FN2:
        rgblight_setrgb (0xFF,  0x00, 0x00);
        break;
    case _FN3:
        rgblight_setrgb (0x7A,  0x00, 0xFF);
        break;
    default:
        rgblight_setrgb (0x00,  0xFF, 0xFF);
        break;
    }
  return state;
}